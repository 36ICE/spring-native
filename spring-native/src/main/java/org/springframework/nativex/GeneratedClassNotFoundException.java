package org.springframework.nativex;

/**
 * @author Sebastien Deleuze
 */
public class GeneratedClassNotFoundException extends RuntimeException {

	public GeneratedClassNotFoundException() {
	}

	public GeneratedClassNotFoundException(String message) {
		super(message);
	}

	public GeneratedClassNotFoundException(String message, Throwable cause) {
		super(message, cause);
	}

	public GeneratedClassNotFoundException(Throwable cause) {
		super(cause);
	}
}
